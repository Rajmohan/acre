    public void setStatus(String message) {
        this.progressMessageLabel.setText(message);
        this.progressMessageLabel.revalidate();
    }

    public void clearStatus() {
        this.progressMessageLabel.setText("");
        this.progressMessageLabel.revalidate();
    }

    public void setProgressBar(int percentage) {
        this.salsaProgress.setMaximum(100);
        this.salsaProgress.setValue(percentage);
        this.revalidate();
    }

    public void hideProgressBar(boolean hide) {
        if (hide)
            this.salsaProgress.setVisible(false);
        else
            this.salsaProgress.setVisible(true);
    }

    public void show(int howManySeconds) {
        int showTime=5;

        // min 5 secs and max 30 secs showing
        if ((howManySeconds < 5) || (howManySeconds > 30))
            showTime=5;

        setVisible(true);
        try {Thread.sleep(showTime *1000); } catch (Exception e) {
            e.printStackTrace();
        }
        setVisible(false);

    }

